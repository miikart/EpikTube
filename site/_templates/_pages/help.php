					<a href="#top">top</a>
				</li>
			</ul>
			
			<ul>	
				<li>
					<strong><a name="copyrightpolicy">What is your policy on copyright infringement?</a></strong>
					<br/>
					EpikTube respects the rights of copyright holders and publishers and requires all members to affirm that they hold the copyright or have permission for the content that they upload. If we receive a notice that content infringes another party's copyright, the member's account may be terminated and the content will be removed from EpikTube.
					<br/>
					<a href="#top">top</a>
				</li>
			</ul>

			<ul>	
				<li>
					<strong><a name="preventviolations">What are you doing to prevent copyrighted content from appearing in EpikTube?</a></strong>
					<br/>
					Since all content on EpikTube is posted by our members, we make it clear in our terms of use and at the time of upload that they must own the copyright to the videos they  post or have permission from the copyright holder. We take copyright issues very seriously and encourage copyright holders to <a href="/t/dmca_policy">contact us</a> if they have any objections about specific videos&#8212;we will cooperate with them to take down any infringing content immediately. Repeat and flagrant offenders of the terms of use are addressed with proprietary technology.  
