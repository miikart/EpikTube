		<h2 class="highlight">Uploading and Making Videos:</h2>
			
			<ul>
				<li>
					<strong><a name="makevideo">How do I make a video?</a></strong>
					<br/>
					For starters you'll need to have a digital camera or some way to create and capture the movies somehow. Then, once you have created a movie of your own you can edit it or add music. We encourage you to edit your videos with software such as Windows MovieMaker (included with every Windows installation), or Apple iMovie. Using these programs you can easily edit your videos, add soundtracks, etc.
					<br/>
					<a href="#top">top</a>
				</li>
			</ul>					
		
			<ul>
				<li>
					<strong><a name="wmm">What is this .MSWMM file? How do I create a movie with Windows Movie Maker?</a></strong>
					<br/>
					A .MSWMM file is a Windows Movie Maker Project file. By saving the project, it allows you to return to the movide and make modifications at a later time. However, if you're satisfied with the movie and would like to upload and share the result through EpikTube, you will need to choose the "Save Movie File" option under the "File" menu (which is different from "Save Project" that creates the .MSWMM file).
					<br/>
					<a href="#top">top</a>
				</li>
			</ul>	
			
