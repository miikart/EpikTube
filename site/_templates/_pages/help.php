		<h2 class="highlight">Privacy, Policy and Copyright Issues:</h2>
			
			<ul>	
				<li>
					<strong><a name="preventemailviews">How can I prevent other users from seeing my email address?</a></strong>
					<br/>
					Rest assured, EpikTube never displays your personal email address, nor is your email address ever displayed when another user sends you a message through the site. The only personal information a user will see about you is what you post on your profile page, as well as the public videos you upload. 
