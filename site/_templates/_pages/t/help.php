<h2 class="pgTitle">General EpikTube Questions</h2>
<dl>
	<dt><a name="whatis">What is EpikTube?</a></dt>
	<dd>EpikTube is a free online video streaming service that allows users to view and share videos that have been uploaded by our members. [<a href="#top">back to top</a>]</dd>
	
	<dt><a name="reallyfree">Is EpikTube really free?</a></dt>
	<dd>Yes. Anyone can view and share videos, and members can upload videos and use other features, free of charge.</dd>

	<dt><a name="watchsave">How do I watch and save videos?</a></dt>
	<dd>Watching videos on EpikTube couldn't be easier&#8212;just come to the site and browse around, or search for subjects that interest you. To save your favorites for later, you need to <a href="/signup">sign up</a> as a member, and then click the 'Add to Favorites' link underneath the videos you want to watch later. You can also create playlists to group favorite videos toether into your own personal catogries.</dd>

	<dt><a name="whattags">What are tags?</a></dt>
	<dd>Tags are keywords that describe videos. For example, a surfing video might be tagged with "surfing," "water," and "waves." Users who enjoy watching surfing videos can then search for any of those terms and that video will pop up in their search results. Tags help you find new videos you might like to watch, and also help you label videos you upload so that other people can find them more easily. The bigger the tag appears in the list, the more videos use that tag.</dd>

	<dt><a name="removevideo">How do I remove a video?</a></dt>
	<dd>To remove one of your videos, simply go to <a href="/my_videos">http://www.epiktube.xyz/my_videos</a> and click on the 'Remove Video' button next to the video you'd like to remove. You are only able to remove videos you have uploaded.</dd>

	<dt><a name="changepassword">How do I change my password?</a></dt>
	<dd>To change your password:
		<ol>
			<li>Login to your EpikTube account.</li>
			<li>Click on the 'My Profile' link under the 'Home' tab.</li>
			<li>Change the two password fields on this page under 'Account Information'.</li>
			<li>Click the 'Update Profile' button at the bottom of the page.</li>
		</ol>
	</dd>

	<dt><a name="clearhistory">How do I clear my search history on EpikTube?</a></dt>
	<dd>EpikTube doesn't save your searches, but your browser probably does.  If you don't want to see your previous search terms displayed in the search field, you need to clear the search history in your browser.</dd>
	
	<dt><a name="changeusername">Can I change my username?</a></dt>
	<dd>No. Your username is your permanent identity in the EpikTube community.</dd>
	
	<dt><a name="deleteothervideo">How do I edit or delete a comment I made on some one else's video?</a></dt>
	<dd>You can't edit or delete comments you make on other people's videos. Once you've made a comment, it can only be removed or moderated by the video owner.</dd>
	
	<dt><a name="featuredvideos">How do videos get featured?</a></dt>
	<dd>EpikTube's users flag videos they like, using the 'Feature This!' link underneath every video. We review all of the flagged videos for consideration in 'Today's Featured Videos'.</dd>
</dl>

	
<h2 class="pgTitle">Making and Uploading Videos</h2>
<dl>
	<dt><a name="makevideo">How do I make a video?</a></dt>
	<dd>You'll need to have a device that can capture digital movies&#8212;this could be a camcorder, digital camera, or even your cell phone! You copy the movies to you computer, and then you can either upload them as-is or edit them with software such as iMovie or Windows MovieMaker to add titles and special effects.</dd>

	<dt><a name="wmm">What is a .MSWMM file? How do I create a movie with Windows Movie Maker?</a></dt>
	<dd>A .MSWMM file is a Windows Movie Maker Project file. By saving the project, it allows you to return to the movie and make modifications at a later time. However, if you're satisfied with the movie and would like to upload and share the result through EpikTube, you will need to choose "Save Movie File" under the "File" menu (which is different from "Save Project", which creates the .MSWMM file).</dd>

	<dt><a name="memberupload">Do I have to sign up to upload videos?</a></dt>
	<dd>Yes. Since videos are attached to your profile, you must sign up as a member to upload videos. Becoming a member also allows you to save videos, create playlists, and leave comments, among many other features.</dd>

	<dt><a name="howlong">How long do my videos stay on EpikTube?</a></dt>
	<dd>Videos stay on EpikTube until the members who uploaded them choose to take them down. Videos which violate the <a href="/t/terms">terms of use</a> and have been flagged may be taken down by EpikTube.</dd>

	<dt><a name="videolength">How long can my video be?</a></dt>
	<dd>Most videos on EpikTube are under five minutes long, but you are limited to 10 minutes per 
video unless you are a Premium Content Provider. Also, the longer your video is, the lower quality it will 
be, due to the compression required.</dd>

	<dt><a name="videoformats">What video file formats can I upload?</a></dt>
	<dd>EpikTube accepts video files from most digital cameras and camcorders, and cell phones in the .AVI, .MOV, and .MPG file formats.</dd>

	<dt><a name="uploadtime">How long will my video take to upload?</a></dt>
	<dd>Depending on your connection speed and the size of the video, it can take anywhere from a few minutes to several hours to upload a video. Generally, if you have a high-speed internet connection you can expect the upload to take somewhere between 1-5mins for every 1MB. However, the experience is different for every user because of the variation in connection speeds.</dd>

	<dt><a name="uploadlimit">Is there a limit to how much I can upload?</a></dt>
	<dd>Currently, the only limits are 10 minutes in length and 100MB per video. We don't have any limit for the number of videos you can upload, and don't foresee adding a ceiling anytime in the future. So get crankin' and upload as many videos as you'd like&#8212;it's more content for the EpikTube community to enjoy!</dd>
</dl>

<h2 class="pgTitle">Embedding and Sharing Videos</h2>
<dl>
	<dt><a name="sharewebsite">How can I share my videos and link to them from my website or blog?</a></dt>
	<dd>Sharing a video is simple&#8212;just click on the 'Share' button at the bottom of the video player, and enter the email addresses of the people to whom you want to send it. Go to <a href="/sharing">http://www.epiktube.xyz/sharing</a> for information on how to link to or embed your videos on Friendster/MySpace/eBay/blogs.</dd>

	<dt><a name="autoplay">Can I make my embedded videos autoplay?</a></dt>
	<dd>Yes, you can autoplay movies! At the bottom of every video, we give you the source code to embed the video:<br/><br/>
		<div class="codeBox">
			&lt;object width=&quot;425&quot; height=&quot;350&quot;&gt;&lt;param name=&quot;movie&quot; value=&quot;http://www.epiktube.xyz/v/OdT9z-JjtJk&quot;&gt;&lt;/param&gt;&lt;embed src=&quot;http://www.epiktube.xyz/v/OdT9z-JjtJk&quot; type=&quot;application/x-shockwave-flash&quot; width=&quot;425&quot; height=&quot;350&quot;&gt;&lt;/embed&gt;&lt;/object&gt;
		</div><br/>
		To make it autoplay, just append "&amp;autoplay=1" to the code so it looks like this:<br/><br/>
		<div class="codeBox">
			&lt;object width=&quot;425&quot; height=&quot;350&quot;&gt;&lt;param name=&quot;movie&quot; value=&quot;http://www.epiktube.xyz/v/OdT9z-JjtJk&amp;autoplay=1&quot;&gt;&lt;/param&gt;&lt;embed src=&quot;http://www.epiktube.xyz/v/OdT9z-JjtJk<strong>&amp;autoplay=1</strong>&quot; type=&quot;application/x-shockwave-flash&quot; width=&quot;425&quot; height=&quot;350&quot;&gt;&lt;/embed&gt;&lt;/object&gt;
		</div>
	</dd>

	<dt><a name="weightheight">The width and height of the video is appearing when I embed it on myspace. How do I remove this?</a></dt>
	<dd>You will need to edit the code that you used to embed your video onto this page.  Go to where you can edit the code, and then remove this text from the beginning:<br/><br/>
		<div class="codeBox">&lt;object width=&quot;425&quot; height=&quot;350&quot;&gt;</div><br/>
		Then save the changes and you will find that this is no longer appearing near your video.</dd>
</dl>
	
	
<h2 class="pgTitle">Privacy, Policy and Copyright Issues</h2>
<dl>
	<dt><a name="preventemailviews">How can I prevent other users from seeing my email address?</a></dt>
	<dd>EpikTube never displays your personal email address, nor is your email address ever displayed when another user sends you a message through the site. The only personal information a user will see about you is what you post on your profile page, as well as the public videos you upload.</dd>

	<dt><a name="copyrightpolicy">What is your policy on copyright infringement?</a></dt>
	<dd>EpikTube respects the rights of copyright holders and publishers and requires all members to affirm that they hold the copyright or have permission for the content that they upload. If we receive a notice that content infringes another party's copyright, the member's account may be terminated and the content will be removed from EpikTube.</dd>

	<dt><a name="preventviolations">What are you doing to prevent copyrighted content from appearing in EpikTube?</a></dt>
	<dd>Since all content on EpikTube is posted by our members, we make it clear in our terms of use and at the time of upload that they must own the copyright to the videos they  post or have permission from the copyright holder. We take copyright issues very seriously and encourage copyright holders to <a href="/t/dmca_policy">contact us</a> if they have any objections about specific videos&#8212;we will cooperate with them to take down any infringing content immediately. Repeat and flagrant offenders of the terms of use are addressed with proprietary technology.</dd>

	<dt><a name="copyrightreporting">How do I report copyright infringement?</a></dt>
	<dd>If you believe that someone else has uploaded your copyrighted content without your permission, please <a href="/t/dmca_policy">contact us</a> to submit a copyright notification.</dd>

	<dt><a name="falsecopyright">What if I have been falsely accused of copyright infringement?</a></dt>
	<dd>We'll let you know if we receive a copyright complaint about any of your video content that is hosted on EpikTube. We'll also give you the opportunity to file a DMCA counter-notification.</dd>

	<dt><a name="inappropriatecontent">How does EpikTube handle inappropriate content?</a></dt>
	<dd>Our community understands the terms of use and effectively polices the site for inappropriate content (similar to other open internet communities such as eBay and craigslist). Their vigilance combined with our proprietary technology helps us to enforce our community standards.</dd>
</dl>
	

<h2 class="pgTitle">Troubleshooting</h2>
<dl>
	<dt><a name="loggingin">It keeps asking me to login, what should I do?</a></dt>
	<dd>Make sure you have cookies enabled in your browser preferences. If you have cookies disabled, there is no way for our system to know you were at the site before, and you will be asked to login.</dd>

	<dt><a name="videosnoplay">The videos won't play&#8212;what's wrong?</a></dt>
	<dd>All of the videos on EpikTube are streamed through a Flash player, so you need to have the latest version of Macromedia Flash installed on your computer. To download it, please visit <a href="http://macromedia.com/shockwave/download/" target="_blank">Adobe</a></dd>
    

	<dt><a name="videostopping">The video keeps stopping while I'm watching it. How do I fix that?</a></dt>
	<dd>If a video doesn't play continuously it may be due to your connection speed. You'll need a broadband connection with at least 500+Kbps for the best viewing experience.</dd>

	<dt><a name="noaudio">Why can't I hear the audio when I'm watching a video?</a></dt>
	<dd>To make sure that you hear sound while watching videos, please check the volume controls on your computer. Also, be sure to increase the volume on other conventional video players on your computer, such as Real Player or Windows Media. This may have an effect on your computer's master volume levels. Next, check to see if your system meets these requirements for running the Video Viewer:<br/><br/>
		<ul>
			<li>Macromedia Flash Player 7.0+ plug-in</li>
			<li>Windows 2000 or higher with latest updates installed</li>
			<li>Mac OS X 10.3 or higher</li>
			<li>Firefox 1.1+, IE 5.0+, or Safari 1.0+</li>
			<li>Broadband connection with 500+ Kbps</li>
		</ul>
		<br/>If your system meets all the requirements, but the Video Viewer still isn't working properly, please try troubleshooting with a different browser. Also, please test the audio on multiple videos.</dd>

	<dt><a name="foreverupload">My video is taking a long time to upload. Is something wrong?</a></dt>
	<dd>Generally, if you have a high-speed internet connection you can expect the upload to take somewhere between 1-5mins for every 1MB. However, the experience is different for every user because of the variation in connection speeds. If your video is very large, it may take a long time to upload.</dd>

</dl>