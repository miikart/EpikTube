<h1 class="pgTitle">A Word on Safety</h1>

<h2 class="pgTitle">Kids, Get Your Parents' Permission!</h2>

<p>The first thing everyone needs to know is that EpikTube is NOT for people under the age of 13, at all. If you are under 13 years of age, please do not visit EpikTube. Minors ages 13-17 are welcome, if they have permission from a parent or legal guardian.</p>

<h2 class="pgTitle">Some Things Are Better Left Private...</h2>

<p>Posting videos of yourself, your friends, or your family can be fun and exciting. You never know who will find it&#8212;it might even get featured on the front page, or wind up on the top of the Most Viewed! If your video is personal, consider marking it private so that only your friends and those you share it with can view it. <a href="javascript:launchWin()">Here's how to do it!</a></p>

<h2 class="pgTitle">Protect Your Secret Identity!</h2>

<p>If you do post public videos, make sure there isn't anything in it that could help a stranger figure out who you are or where you live. Personal information like your telephone number or home address should NEVER be shared with other users. Watch out for things like license plate numbers on cars or images of the outside of your home which might accidentally appear in the background of a video and help a stranger to track you down.</p>

<p>Remember, EpikTube employees will never ask you for your password, email address, or other account information. Don't be fooled if someone contacts you pretending to be from EpikTube! If in doubt, you can always send a message to us by filling out <a href="/contact">this form</a> on the EpikTube website.</p>

<h2 class="pgTitle">Keep Your Cool, Keep EpikTube Safe</h2>

<p>Whether it's a flame war, cyberbullying, or people being just plain mean, comments can get pretty rough sometimes. When you post a video you can choose whether you want to let people comment on your videos at all. If someone leaves a comment on your video that is mean or bothers you, you can always delete the comment. Another option is to have comments "kinda" on, which means that you get to approve or remove people's comments before they appear on the site. <a href="javascript:launchWin()">Here's how to do it!</a></p>

<p>EpikTube doesn't allow videos with nudity, graphic violence, or hate. If you come across a video like this, click the link on the video to flag it as Inappropriate and submit the form on the next page to report it to EpikTube.</p>

<p>EpikTube has MILLIONS of viewers every day and just like in the real world, most of the people are good. However, you should always take care to protect yourself and your fellow users by keeping personal videos private, your identity a secret, your comments clean, and by using the flagging system to report abuse.</p>

<p><strong>Most importantly, make sure your parents know you are using EpikTube and if you have any questions or if something on the site bothers you, let them know right away!</strong></p>

